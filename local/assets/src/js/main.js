/* Плавный скролл по странице по id */

window.addEventListener('hashchange', () => {
  const anchor = window.location.hash;

  if (anchor) {
    const target = document.getElementById(anchor.substring(1));
    if (target) {
      target.scrollIntoView({
        behavior: 'smooth',
        block: 'start',
        inline: 'nearest',
      });
    }
  }
});

